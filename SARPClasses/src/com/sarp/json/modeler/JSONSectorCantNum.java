package com.sarp.json.modeler;

public class JSONSectorCantNum {

	private String codigo;
	private String nombre;
	private int cantNumEspera;
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getCantNumEspera() {
		return cantNumEspera;
	}
	public void setCantNumEspera(int cantNumEspera) {
		this.cantNumEspera = cantNumEspera;
	}
	
}
